public class main {
  public static void main(String[] args){

    double myDouble = 3.4;
    float myFloat = 3.4F;

    // 3.445 * 10^2
    double myDoubleScientific = 3.44e2;

    System.out.println(myDouble); // prints 3.4
    System.out.println(myFloat); // prints 3.4
    System.out.println(myDoubleScientific); // 344.0
  }
  
}
